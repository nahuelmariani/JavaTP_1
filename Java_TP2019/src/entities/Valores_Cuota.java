package entities;
import java.util.*;

public class Valores_Cuota {
	private int idValoresCuota;
	private Date fecha;
	private Double valor;
	
	public int getIdValoresCuota() {
		return idValoresCuota;
	}
	public void setIdValoresCuota(int idValoresCuota) {
		this.idValoresCuota = idValoresCuota;
	}
	public Date getFecha() {
		return fecha;
	}
	public void setFecha(Date fecha) {
		this.fecha = fecha;
	}
	public Double getValor() {
		return valor;
	}
	public void setValor(Double valor) {
		this.valor = valor;
	}
	
}
